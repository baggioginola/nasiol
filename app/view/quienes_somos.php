        <h1>About Us</h1>
        <p>Nasiol is the registered trade mark of Artekya Ltd. Co., which is the main and pioner&nbsp;manufacturer&nbsp;of protective nano coating materials in Turkey and Europe.&nbsp;</p>

        <p>Artekya is an off-shoot of Istanbul Technical University’s NanoFMG Group, which has studied nanotechnology since 2005. The group collaborated on many projects with the government. From then to the present, Artekya has accomplished and continues to develop effective and innovative products for commercial and easy-spray household use under the brand Nasiol.</p>

        <p>Our high quality technology which we call it "Smart Silis Tech"&nbsp;means economy in the consumption of raw materials and&nbsp;leads to less environmental impact while increasing performance. Smart silis technology&nbsp;provides easy cleaning, hydrophobicity and protection on all kind of surfaces that is&nbsp;developed with ultra-thin SiO2-based nano-coatings.</p>

        <p><strong><span style="line-height: 1.2em;">History and Achievements</span></strong></p>

        <p>The company was founded in 2008, a R&amp;D company working on nanospinning techniques and machines, the first and only in Turkey and amongst only three others in the world.</p>

        <p>In 2012, R&amp;D on protective nano coatings started and NanoFMG Group changed its name to Artekya.</p>

        <p>Under Artekya, we created the brand Nasiol for our liquid nano coatings, the first ever manufactured in the country. Explore our milestones below.</p>

