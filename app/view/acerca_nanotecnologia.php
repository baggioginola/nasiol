        <h1>About Nanotechnology</h1>
        <p>Physics, chemistry and engineering working together on materials at their nano scale--that’s nanotechnology. The word "nano" refers to a unit prefix of one billionth, and nanoscale is at 1-100 nanometers, used in science to define length and time.&nbsp;&nbsp;</p>

        <p>Nano particles are synthesized separately to the desired size and shape, giving them extraordinary ability and features. Nano level is molecular and atomic, where we see so much more of the behaviour of materials, thus leading to discoveries that in turn lead to developments and benefits superior to what can be achieved with conventional science.</p>

        <p>Nobel Prize-winning Richard Feynman, the world-renowned physicist who introduced the concept of nanotechnology, sums up this revolutionary field in one sentence: &nbsp;“There’s plenty of room at the bottom.”</p>

        <p>“Nanotechnology” was first coined by Professor Norio Taniguchi in 1974. Nanotechnology studies upticked after 2000, and so did funding.&nbsp;</p>

