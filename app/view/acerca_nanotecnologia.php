        <h2>Fields of Application</h2>

        <p><strong>Medicine</strong></p>

        <p>Researchers are developing drugs from nanoparticles for diseased cells.</p>

        <p><strong>Electronics</strong></p>

        <p>Electronic devices are getting smaller and durable with use of nanotechnology.</p>

        <p><strong>Energy</strong></p>

        <p>From solar panels to batteries, nanotech structures are utilized to develop and increase energy-efficiency.</p>

        <p><strong>Food Industry</strong></p>

        <p>Companies are developing nano-sized fertilizers for better and targeted performance.</p>

        <p><strong>Coatings </strong></p>

        <p>Nanocoatings ensure superior lifetime and specifications in the protective coating industry.</p>

        <p><strong>Space</strong><strong> industry</strong></p>

        <p>Extra-light and durable materials are developed for use in space.</p>

        <p><strong>Environment</strong></p>

        <p>Nano-sized filters give unique filtration to avoid air and water pollution. Water sources polluted for years are cleaned by nanotechnology.&nbsp;</p>

